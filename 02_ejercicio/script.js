//switch case

let dia = Number(prompt('Ingrese número'));

switch(dia){
    case 0: 
    alert('El día es domingo');
    break;

    case 1: 
    alert('El día es lunes');
    break;

    case 2: 
    alert('El día es martes');
    break;

    default:
        alert('El día ingresado no es válido');
    break;
}
